#!/usr/bin/env python3
"""
Multi-model comparison using knowledge bases generated by Claude Haiku 4.5,
Sonnet 4.6 and Opus 4.8 through the Claude Code agent interface (no paper API
key consumed). Each agent wrote agent_gen/<model>.json from its own knowledge.

This script converts those JSON files to Prolog, validates a sample of each
model's facts against Wikidata with the same type-aware linker as the main
experiment, and reports a per-model accuracy / coverage comparison.
"""
import os
import re
import csv
import json
import random
from collections import defaultdict

import validate_corpus as V

random.seed(20260614)
GEN = os.path.join(V.HERE, "agent_gen")
OUT = os.path.join(V.HERE, "results_agents")
os.makedirs(OUT, exist_ok=True)
MODELS = ["haiku", "sonnet", "opus"]
PER_MODEL = 70   # verifiable facts validated per model (bounded Wikidata calls)


def clean_atom(text):
    c = re.sub(r"[^\w]", "", str(text).lower().replace(" ", "_"))
    if c and c[0].isdigit():
        c = "x" + c
    return c or "unknown"


def json_to_facts(blob):
    """blob: {topic: {concepts:[...], relationships:[{source,relation,target}]}}"""
    by_topic = {}
    for topic, d in blob.items():
        facts = [f"concept({clean_atom(topic)})."]
        for c in d.get("concepts", []):
            facts.append(f"concept({clean_atom(c)}).")
            facts.append(f"related_to({clean_atom(c)}, {clean_atom(topic)}).")
        for r in d.get("relationships", []):
            try:
                facts.append(f"{clean_atom(r['relation'])}({clean_atom(r['source'])}, {clean_atom(r['target'])}).")
            except Exception:
                pass
        by_topic[clean_atom(topic)] = facts
    return by_topic


def load_blob(path):
    txt = open(path, encoding="utf-8").read()
    m = re.search(r"```json\s*([\s\S]*?)```|(\{[\s\S]*\})", txt)
    raw = (m.group(1) or m.group(2)) if m else txt
    return json.loads(raw)


def main():
    wd = V.Wikidata()
    rows = []
    per_fact = []
    for model in MODELS:
        path = os.path.join(GEN, f"{model}.json")
        if not os.path.exists(path):
            print(f"[skip] {path} not found")
            continue
        blob = load_blob(path)
        by_topic = json_to_facts(blob)
        # write .pl files (real artifacts)
        mdir = os.path.join(OUT, model)
        os.makedirs(mdir, exist_ok=True)
        verifiable = []
        total_facts = 0
        for topic, facts in by_topic.items():
            total_facts += len(facts)
            with open(os.path.join(mdir, f"{topic}.pl"), "w") as f:
                f.write("\n".join(facts) + "\n")
            for line in facts:
                mm = V.FACT_RE.match(line)
                if mm and mm.group(1) in V.PRED_SPEC and mm.group(2).strip() != mm.group(3).strip():
                    verifiable.append((topic, mm.group(1), mm.group(2).strip(), mm.group(3).strip()))
        random.shuffle(verifiable)
        sample = verifiable[:PER_MODEL]
        counts = defaultdict(int)
        for topic, pred, a, b in sample:
            st, *_ , det = V.verify(wd, pred, a, b, V.link_improved)
            counts[st] += 1
            per_fact.append([model, topic, pred, a, b, st, det])
        wd.save()
        ver, con = counts["verified"], counts["contradicted"]
        chk = ver + con
        acc, lo, hi = V.wilson(ver, chk)
        rows.append(dict(model=model, topics=len(by_topic), total_facts=total_facts,
                         verifiable=len(verifiable), validated=len(sample),
                         verified=ver, contradicted=con, checkable=chk,
                         accuracy=round(acc, 4), ci_low=round(lo, 4), ci_high=round(hi, 4),
                         link_fail=counts["link_fail"],
                         coverage=round(chk / len(sample), 4) if sample else 0))
        print(f"{model:7s} topics={len(by_topic)} facts={total_facts} verifiable={len(verifiable)} "
              f"| validated={len(sample)} verified={ver} contra={con} "
              f"acc={acc*100:.1f}% [{lo*100:.1f},{hi*100:.1f}] cov={chk/len(sample)*100:.0f}%", flush=True)

    with open(os.path.join(OUT, "comparison.json"), "w") as f:
        json.dump(rows, f, indent=2)
    with open(os.path.join(OUT, "per_fact.csv"), "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["model", "topic", "predicate", "arg1", "arg2", "status", "detail"])
        w.writerows(per_fact)
    print("\nWrote", os.path.join(OUT, "comparison.json"))


if __name__ == "__main__":
    main()
